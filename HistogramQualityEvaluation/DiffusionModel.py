@torch.no_grad()
def p_sample_loop(model, label, shape):
    device = next(model.parameters()).device

    b = shape[0]
    # start from pure noise (for each example in the batch)
    img = torch.randn(shape, device=device)
    imgs = []

    for i in tqdm(reversed(range(0, timesteps)), desc='sampling loop time step', total=timesteps):
        img = p_sample(model, img, label, torch.full((b,), i, device=device, dtype=torch.long), i)
        imgs.append(img.cpu().numpy())
    return imgs


@torch.no_grad()
def sample(model, label, seq_length, batch_size=16, channels=3):
    return p_sample_loop(model,
                         torch.full((batch_size,), label, device=device, dtype=torch.long),
                         (batch_size, channels, seq_length))


