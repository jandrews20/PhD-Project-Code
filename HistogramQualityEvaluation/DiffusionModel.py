def train(model, dataloader, dataloader_val, epochs, optimizer):

    train_loss = 0
    train_iters = 0
    epoch_count = 0

    for epoch in range(epochs):
        for step, batch in enumerate(dataloader):
            optimizer.zero_grad()

            batch_size = batch[0].shape[0]
            labels = batch[1].to(device)
            batch = batch[0].to(device)

            # Algorithm 1 line 3: sample t uniformally for every example in the batch
            t = torch.randint(0, timesteps, (batch_size,), device=device).long()

            loss = p_losses(model, batch, t, labels, loss_type="huber")
            train_loss += loss.item()
            train_iters += 1

            loss.backward()
            optimizer.step()

            if step % 250 == 0:
                val_loss = 0
                val_iters = 0
                with torch.no_grad():
                    for _, val_batch in enumerate(dataloader_val):
                        val_batch_size = val_batch[0].shape[0]
                        val_labels = val_batch[1].to(device)
                        val_batch = val_batch[0].to(device)
                        t_val = torch.randint(0, timesteps, (val_batch_size,), device=device).long()
                        val_loss += p_losses(model, val_batch, t_val, val_labels, loss_type="huber").item()
                        val_iters += 1
                    print("Epoch:", epoch, " Loss:", train_loss / train_iters, " Val Loss:", val_loss / val_iters)
                    torch.save(model.state_dict(), f"./results/model{epoch}.pt")
                train_loss = 0
                train_iters = 0




